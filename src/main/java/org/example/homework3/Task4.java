package org.example.homework3;

public class Task4 {
    public static void main(String[] args) {

        // Declare and initialize two integer variables
        int dividend_A = 7;
        int divisor_B = 5;

        // Calculate the integer part of the division
        int integerPart = dividend_A / divisor_B;

        // Calculate the remainder after division
        int remainder = dividend_A % divisor_B;

        // Print the integer part and remainder
        System.out.println("Integer part of division: " + integerPart);
        System.out.println("Remainder after division: " + remainder);
    }
}
